/*2) Create a java program that takes four variables like 
integer, double, float and character and also display their 
values.*/

class t1_2 {
	public static void main(String[] args) {

		int a = 10;
		double d = 20;
		float f = 3.4f;
		char c = 'A';
		System.out.println("int" + a);
		System.out.println("double" + d);
		System.out.println("float" + f);
		System.out.println("char" + c);
	}
}